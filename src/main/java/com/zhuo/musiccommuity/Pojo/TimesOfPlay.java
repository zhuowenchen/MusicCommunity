package com.zhuo.musiccommuity.Pojo;

public class TimesOfPlay {
    long id;
    long timesOfPlay;
    int sid;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public long getTimesOfPlay() {
        return timesOfPlay;
    }

    public void setTimesOfPlay(long timesOfPlay) {
        this.timesOfPlay = timesOfPlay;
    }

    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }
}
